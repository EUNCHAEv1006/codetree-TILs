public class Main {
    public static void main(String[] args) {
        double ft = 30.48;
        double mi = 160934;

        double realFT = 9.2*ft;
        double realMI = 1.3*mi;

        System.out.print("9.2ft = ");
        System.out.printf("%.1f", realFT);
        System.out.println("cm");

        System.out.print("1.3mi = ");
        System.out.printf("%.1f", realMI);
        System.out.println("cm");
    }
}